INSTALL_DIR="${DEVI_INSTALL_DIR:-$HOME/.devi-ops}"
APP_PORT="${PORT:-3000}"
DB_PORT="${DB_PORT:-5432}"
DB_USER="${DB_USER:-wait_what}"
DB_PASSWORD="${DB_PASSWORD:-wait_what}"
